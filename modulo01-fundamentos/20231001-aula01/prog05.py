# Operadores aritméticos

if __name__ == "__main__":

    # Operador Adição (+)
    print(1 + 1)

    # Operador Subtração (-)
    print(10 - 2)

    # Operador Multiplicação (*)
    print(2 * 3)

    # Operador divisão (/)
    print(20 / 4)

    # Operador exponenciação (**)
    print(3 ** 3)

    # Operador resto da divisão (%)
    print(31 % 2)

    # Operador divisão inteira (floor division) (//)
    print(10 // 3)
