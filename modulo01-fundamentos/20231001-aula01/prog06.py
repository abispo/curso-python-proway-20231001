# Operadores de comparação

if __name__ == "__main__":

    # Igual (==)
    # Verifica se o valor da esquerda é igual ao valor da direita
    print(20 == 19)

    # Diferente (!=)
    # Vericica se o valor da esquerda é diferente do valor da direita
    print("CHECADO" != "NAO_CHECADO")

    # Maior que (>)
    # Verifica se o valor da esquerda é maior que o valor da direita
    print(4 > 3)

    # Menor que (<)
    # Verifica se o valor da esquerda é menor do que o valor da direita
    print(10 < 13)

    # Maior ou igual a (>=)
    # Verifica se o valor da esquerda é maior ou igual ao valor da direita
    print(5 >= 5)

    # Menor ou igual a (<=)
    # Verifica se o valor da esquerda é menor ou igual ao valor da direita
    print(8 <= 7)