# Operadores lógicos

if __name__ == "__main__":
    
    # Operador AND
    # Retorna True caso os dois lados da comparação retornem True. Caso contrário, retorna
    # False
    print(10 > 6 and 4 > 3)

    # Operador OR
    # Retorna True caso quaisquer um dos lados da comparação retorne True. Caso contrário,
    # retorna False
    print(10 < 9 or 5 >= 5)

    # Operador Not
    # Retorna a negação de um valor. Caso o valor for True, retorna False. Caso o valor seja
    # False, retorna True
    print(not 10 > 11)